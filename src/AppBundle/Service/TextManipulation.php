    /**
     * Clear text
     *
     * @param $text
     * @return string
     */
    public function cleanText($text) : string
    {
        $text = str_replace("&lt;", "<", $text);
        $text = str_replace("&gt;", ">", $text);
        $text = str_replace("&nbsp;", "", $text);
        $text = str_replace("&quot;", '"', $text);

        return preg_replace('/<script\b[^>]*>(.*?)<\/script>/is', "", $text);
    }

    /**
     * Html Decode
     *
     * @param string $text
     * @return string
     */
    public function htmlDecode(string $text) : string
    {
        return htmlspecialchars_decode($text);
    }

    /**
     * Remove javascript tags from html
     *
     * @param string $text
     * @return string
     */
    public function removeJS(string $text) : string
    {
        $text = str_replace("&lt;", "<", $text);
        $text = str_replace("&gt;", ">", $text);
        $text = str_replace("&nbsp;", "", $text);

        return preg_replace('/<script\b[^>]*>(.*?)<\/script>/is', "", $text);
    }

    /**
     * Convert number of seconds into hours, minutes and seconds
     * and return an array containing those values
     *
     * @param integer $inputSeconds Number of seconds to parse
     * @return array
     */

    function secondsToTime($inputSeconds) {

        $secondsInAMinute = 60;
        $secondsInAnHour  = 60 * $secondsInAMinute;
        $secondsInADay    = 24 * $secondsInAnHour;

        // extract days
        $days = floor($inputSeconds / $secondsInADay);

        // extract hours
        $hourSeconds = $inputSeconds % $secondsInADay;
        $hours = floor($hourSeconds / $secondsInAnHour);

        // extract minutes
        $minuteSeconds = $hourSeconds % $secondsInAnHour;
        $minutes = floor($minuteSeconds / $secondsInAMinute);

        // extract the remaining seconds
        $remainingSeconds = $minuteSeconds % $secondsInAMinute;
        $seconds = ceil($remainingSeconds);

        // return the final array
        $obj = array(
            'd' => (int) $days,
            'h' => (int) $hours,
            'm' => (int) $minutes,
            's' => (int) $seconds,
        );
        return $obj;
    }

    function seconds2human($ss) {
        $s = $ss%60;
        $m = floor(($ss%3600)/60);
        $h = floor(($ss%86400)/3600);
        $d = floor(($ss%2592000)/86400);
        $M = floor($ss/2592000);

        return "$M months, $d days, $h hours, $m minutes, $s seconds";
    }
